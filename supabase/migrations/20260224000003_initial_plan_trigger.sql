GRANT EXECUTE ON FUNCTION public.ensure_weekly_plan_exists(UUID) TO authenticated;
GRANT EXECUTE ON FUNCTION public.ensure_weekly_plan_exists(UUID) TO service_role;

-- ============================================================================
-- COMMENTS
-- ============================================================================

COMMENT ON FUNCTION public.ensure_weekly_plan_exists(UUID) IS 
'Checks if a weekly plan exists for the current week. If not, creates a snapshot and returns status indicating plan generation is needed.
This function is called by the app when user first accesses the plan screen to ensure they have a plan even if they signed up mid-week.';



